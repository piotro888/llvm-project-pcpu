#ifndef LLVM_LIB_TARGET_PCPU_PCPUSUBTARGET_H
#define LLVM_LIB_TARGET_PCPU_PCPUSUBTARGET_H

#include "PCPUFrameLowering.h"
#include "PCPUISelLowering.h"
#include "PCPUInstrInfo.h"
#include "PCPUSelectionDAGInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "PCPUGenSubtargetInfo.inc"

namespace llvm {

class PCPUSubtarget : public PCPUGenSubtargetInfo {
public:
  // This constructor initializes the data members to match that
  // of the specified triple.
  PCPUSubtarget(const Triple &TargetTriple, StringRef Cpu,
                 StringRef FeatureString, const TargetMachine &TM,
                 const TargetOptions &Options, CodeModel::Model CodeModel,
                 CodeGenOpt::Level OptLevel);

  // ParseSubtargetFeatures - Parses features string setting specified
  // subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  PCPUSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  void initSubtargetFeatures(StringRef CPU, StringRef FS);

  bool enableMachineScheduler() const override { return true; }

  const PCPUInstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const PCPURegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  const PCPUTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }

  const PCPUSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

private:
  PCPUFrameLowering FrameLowering;
  PCPUInstrInfo InstrInfo;
  PCPUTargetLowering TLInfo;
  PCPUSelectionDAGInfo TSInfo;
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_PCPU_PCPUSUBTARGET_H
